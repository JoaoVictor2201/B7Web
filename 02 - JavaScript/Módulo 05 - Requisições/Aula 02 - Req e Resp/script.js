// Request e Response
/* 
    Request = Requisição
    -> Cabeçalhos / Headers - Informações que ajudam o servidor a saber como tratar os dados pedidos (de onde vem a requisição, qual o tipo do aparelho, qual o navegador usado, cookies, etc...)
    -> Corpo da requisição / Body - Informações Brutas (Mensagens digitadas, uma pesquisa feita dentro do site), podem ser vazios no caso de requisições apenas para exibir uma página que não precisa de informações do usuário
*/

/*
    Response = Resposta
    -> Cabeçalhos / Headers - Informações mandadas pelo servidor (Cache)
    -> Corpo / Body - Código HTML para exibição da página
*/