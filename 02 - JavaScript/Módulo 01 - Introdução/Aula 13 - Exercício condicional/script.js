/** Substituir o @ para q dê TRUE */
let x = 10;
let y = 5;
/** console.log(x @ y) */
console.log(x > 5);

/** Substituir o @ para q dê FALSE */
let w = "10";
let z = 10;
/** console.log(w @ z) */
console.log(w === z);

/** Substituir o @ para q dê TRUE */
let a = 10;
let b = 5;
/** console.log(a @ b) */
console.log(a != b);

/** Criar uma condicional para verificar se o preço está caro (Acima de 45) */
let preco = 50;
if (preco <= 45) {
    console.log('Barato');
} else {
    console.log('Caro');
}